    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(comparaCamposAdd())
        {
            con = null;
            conex = new Conexion();
            int stockA = 0, aumentarStock = 0;
            String nombre = cbxNombre.getSelectedItem().toString().trim();
            int cantidad = Integer.parseInt(cbxStock.getSelectedItem().toString().trim());
            String consulta = "SELECT stockPro FROM productos WHERE nombrePro = '"+nombre+"'";
            
            try {
                con = conex.getConnection();
                ps = con.prepareStatement(consulta);
                rs = ps.executeQuery();
                while (rs.next()) 
                {                    
                  stockA = rs.getInt("stockPro");
                }
            } catch (Exception e) {
            }
            aumentarStock = stockA + cantidad;
            String consultaModificar = "UPDATE productos SET stockPro='"+aumentarStock+"' WHERE nombrePro ='"+nombre+"'";
            try 
            {
               ps = con.prepareStatement(consultaModificar);
               int r = ps.executeUpdate();
               if(r>0)
               {
                    JOptionPane.showMessageDialog(null,"El producto "+nombre+" actualizo su Stock a "+aumentarStock,"Actualizar Stock",JOptionPane.INFORMATION_MESSAGE);
                    limpiarCombox();
                    listar();
               }else{
                   JOptionPane.showMessageDialog(null,"Error al actualizar Stock","Actualizar Stock",JOptionPane.ERROR_MESSAGE);
               }
               
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

