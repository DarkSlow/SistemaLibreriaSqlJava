private void cargarDatosAlCombo() {
        con = null;
        conex = new Conexion();
        try {
            con = conex.getConnection();
            ps = con.prepareStatement("SELECT * FROM productos");
            rs = ps.executeQuery();
            while (rs.next()) {
                cbxNombre.addItem(rs.getString("nombrePro"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
private boolean comparaCamposAdd() {
        boolean encontrado = true;
        if ((cbxNombre.getSelectedIndex() == 0) || (cbxStock.getSelectedIndex() == 0)) {
            JOptionPane.showMessageDialog(null, "Seleccione los campos necesarios", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            encontrado = false;
        }
        return encontrado;
    }
private DefaultTableModel setTitulos()
{
    df = new DefaultTableModel();
    df.addColumn("Nombre del Producto");
    df.addColumn("Stock del Producto");
    return df;
}
private DefaultTableModel getDatos()
{
    con = null;
        conex = new Conexion();
        try {
            setTitulos();
            con = conex.getConnection();
            ps = con.prepareStatement("SELECT nombrePro, stockPro FROM productos");
            rs = ps.executeQuery();
            Object obj[] = new Object[2];
            while (rs.next()) {
                obj[0] = rs.getString("nombrePro");
                obj[1] = rs.getInt("stockPro");
                df.addRow(obj);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return df;
}
private void listar() {
        tableData.setModel(getDatos());
    }
private void limpiarCombox(){
    cbxNombre.setSelectedIndex(0);
    cbxStock.setSelectedIndex(0);
}
